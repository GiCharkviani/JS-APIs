// Reminders
console.log(5 % 2); // 1
console.log(5 / 2); // 5 = 2 * 2 + 1 (reminder)

console.log(8 % 3); // 2
console.log(8 / 3); // 8 = 2 * 3 + 2 (reminder)


console.log(6 % 2); // 0
console.log(6 / 2); // 3

console.log(7 % 2); // 1
console.log(7 / 2); // 3.5

// Even or not
const isEven = n => n % 2 === 0;
console.log(isEven(8))
